        [TestMethod]
        public void BuildUsrFrame_ExtendedId_SetsBit7()
        {
            var f = new TrcCanFrame(1.0, 0x18F140CA, true, 8, new byte[] { 1, 2, 3, 4, 5, 6, 7, 8 });
            byte[] usr = TrcConverter.BuildUsrFrame(f);

            Assert.AreEqual(13, usr.Length);
            
            // Info byte: bit7(EXT)=1, DLC=8 => 0x88
            Assert.AreEqual(0x88, usr[0]);
            
            // ID big-endian (29-bit)
            Assert.AreEqual(0x18, usr[1]);
            Assert.AreEqual(0xF1, usr[2]);
            Assert.AreEqual(0x40, usr[3]);
            Assert.AreEqual(0xCA, usr[4]);

            // Data
            Assert.AreEqual(1, usr[5]);
            Assert.AreEqual(8, usr[12]);
        }

        [TestMethod]
        public void BuildUsrFrame_StandardId_ClearsBit7()
        {
            var f = new TrcCanFrame(1.0, 0x1A4, false, 4, new byte[] { 0xAA, 0xBB, 0xCC, 0xDD });
            byte[] usr = TrcConverter.BuildUsrFrame(f);

            Assert.AreEqual(13, usr.Length);
            
            // Info byte: bit7(EXT)=0, DLC=4 => 0x04
            Assert.AreEqual(0x04, usr[0]);
            
            // ID big-endian (29-bit zero padded)
            Assert.AreEqual(0x00, usr[1]);
            Assert.AreEqual(0x00, usr[2]);
            Assert.AreEqual(0x01, usr[3]);
            Assert.AreEqual(0xA4, usr[4]);

            // Data (zero padded to 8 bytes)
            Assert.AreEqual(0xAA, usr[5]);
            Assert.AreEqual(0xDD, usr[8]);
            Assert.AreEqual(0x00, usr[9]);
            Assert.AreEqual(0x00, usr[12]);
        }
        
