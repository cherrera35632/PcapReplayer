        private static string BuildMinimalTrc(IEnumerable<string> rxLines)
        {
            var sb = new StringBuilder();
            sb.AppendLine(";$FILEVERSION=1.1");
            sb.AppendLine(";$STARTTIME=46140.6177158796");
            sb.AppendLine(";");
            sb.AppendLine(";   Start time: 4/28/2026 14:49:30.652.0");
            sb.AppendLine(";   Generated by PCAN-View v6.0.0.1040");
            sb.AppendLine(";");
            sb.AppendLine(";   Message Number");
            sb.AppendLine(";   |         Time Offset (ms)");
            sb.AppendLine(";   |         |        Type");
            sb.AppendLine(";   |         |        |        ID (hex)");
            sb.AppendLine(";   |         |        |        |     Data Length");
            sb.AppendLine(";   |         |        |        |     |   Data Bytes (hex) ...");
            sb.AppendLine(";   |         |        |        |     |   |");
            sb.AppendLine(";---+--   ----+----  --+--  ----+---  +  -+ -- -- -- -- -- -- --");
            int lineNum = 1;
            foreach (var line in rxLines)
            {
                sb.AppendLine($" {lineNum,4}) {line}");
                lineNum++;
            }
            return sb.ToString();
        }

        private static string GetTempFile(string ext)
            => Path.Combine(Path.GetTempPath(), $"trc_test_{Guid.NewGuid():N}{ext}");

        private static List<byte[]> ParsePcapUdpPayloads(string pcapPath)
        {
            var payloads = new List<byte[]>();
            using var fs = File.OpenRead(pcapPath);
            using var br = new BinaryReader(fs);

            // Skip 24-byte global header
            br.ReadBytes(24);

            while (fs.Position < fs.Length)
            {
                // packet header
                br.ReadUInt32(); // ts_sec
                br.ReadUInt32(); // ts_usec
                uint incl_len = br.ReadUInt32();
                br.ReadUInt32(); // orig_len
                byte[] pkt = br.ReadBytes((int)incl_len);

                // Ethernet(14) + IP(20) + UDP(8) = 42 bytes header
                if (pkt.Length > 42)
                {
                    byte[] payload = new byte[pkt.Length - 42];
                    Array.Copy(pkt, 42, payload, 0, payload.Length);
                    payloads.Add(payload);
                }
            }
            return payloads;
        }

