        [TestMethod]
        public void Convert_EmptyFile_ThrowsInvalidOperation()
        {
            string trcPath = GetTempFile(".trc");
            string outPath = GetTempFile(".pcap");
            try
            {
                File.WriteAllText(trcPath, "");
                var opts = new TrcConversionOptions { TrcFile = trcPath, OutputPcap = outPath };
                
                Assert.ThrowsException<InvalidOperationException>(() => TrcConverter.Convert(opts));
            }
            finally
            {
                File.Delete(trcPath);
                if (File.Exists(outPath)) File.Delete(outPath);
            }
        }

        [TestMethod]
        public void Convert_HeaderOnly_ThrowsInvalidOperation()
