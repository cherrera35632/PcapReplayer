        [TestMethod]
        public void Convert_ExceedsBatchThresholdMs_SplitsPackets()
        {
            string trcPath = GetTempFile(".trc");
            string outPath = GetTempFile(".pcap");
            try
            {
                string trcData = BuildMinimalTrc(new[]
                {
                    "        1.0  Rx     100  8  00",
                    "        2.0  Rx     101  8  00", // delta 1.0 (batch 1)
                    "       10.0  Rx     102  8  00", // delta 9.0 > 5.0 (batch 2 starts)
                    "       12.0  Rx     103  8  00", // delta 2.0 (batch 2)
                    "       20.0  Rx     104  8  00"  // delta 10.0 > 5.0 (batch 3 starts)
                });
                File.WriteAllText(trcPath, trcData);

                var opts = new TrcConversionOptions
                {
                    TrcFile = trcPath,
                    OutputPcap = outPath,
                    BatchThresholdMs = 5.0,
                    FramesPerPacket = 100,
                    MetadataHeader = "META"
                };

                var res = TrcConverter.Convert(opts);

                Assert.AreEqual(5, res.FramesParsed);
                Assert.AreEqual(3, res.PacketsWritten);

                var payloads = ParsePcapUdpPayloads(outPath);
                Assert.AreEqual(3, payloads.Count);

                // Batch 1: "META" + 2 frames
                Assert.AreEqual(4 + 26, payloads[0].Length);
                // Batch 2: 2 frames (no meta)
                Assert.AreEqual(26, payloads[1].Length);
                // Batch 3: 1 frame (no meta)
                Assert.AreEqual(13, payloads[2].Length);
            }
            finally
            {
                File.Delete(trcPath);
                if (File.Exists(outPath)) File.Delete(outPath);
            }
        }

        [TestMethod]
        public void Convert_ExceedsFramesPerPacket_SplitsPackets()
        {
            string trcPath = GetTempFile(".trc");
            string outPath = GetTempFile(".pcap");
            try
            {
                // 5 frames all close together in time
                string trcData = BuildMinimalTrc(new[]
                {
                    "        1.0  Rx     100  8  00",
                    "        1.1  Rx     101  8  00",
                    "        1.2  Rx     102  8  00",
                    "        1.3  Rx     103  8  00",
                    "        1.4  Rx     104  8  00"
                });
                File.WriteAllText(trcPath, trcData);

                var opts = new TrcConversionOptions
                {
                    TrcFile = trcPath,
                    OutputPcap = outPath,
                    BatchThresholdMs = 100.0,
                    FramesPerPacket = 2, // limit 2 per packet
                    MetadataHeader = "META"
                };

                var res = TrcConverter.Convert(opts);

                Assert.AreEqual(5, res.FramesParsed);
                // 5 frames / 2 per packet = 3 packets (2, 2, 1)
                Assert.AreEqual(3, res.PacketsWritten);

                var payloads = ParsePcapUdpPayloads(outPath);
                Assert.AreEqual(3, payloads.Count);

                // Batch 1: "META" + 2 frames
                Assert.AreEqual(4 + 26, payloads[0].Length);
                // Batch 2: 2 frames (no meta)
                Assert.AreEqual(26, payloads[1].Length);
                // Batch 3: 1 frame (no meta)
                Assert.AreEqual(13, payloads[2].Length);
            }
            finally
            {
                File.Delete(trcPath);
                if (File.Exists(outPath)) File.Delete(outPath);
            }
        }

