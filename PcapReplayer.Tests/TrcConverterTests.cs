        [TestMethod]
        public void BuildUdpPacket_PopulatesHeadersCorrectly()
        {
            byte[] payload = new byte[] { 0x11, 0x22, 0x33, 0x44 };
            byte[] pkt = TrcConverter.BuildUdpPacket("10.0.0.1", "10.0.0.2", 1234, 5678, payload);
            
            Assert.AreEqual(14 + 20 + 8 + 4, pkt.Length); // 46 bytes

            // Check IP Source (offset 14 + 12 = 26)
            Assert.AreEqual(10, pkt[26]);
            Assert.AreEqual(0, pkt[27]);
            Assert.AreEqual(0, pkt[28]);
            Assert.AreEqual(1, pkt[29]);

            // Check IP Dest (offset 14 + 16 = 30)
            Assert.AreEqual(10, pkt[30]);
            Assert.AreEqual(0, pkt[31]);
            Assert.AreEqual(0, pkt[32]);
            Assert.AreEqual(2, pkt[33]);

            // Check UDP Source Port (offset 34) - 1234 = 0x04D2
            Assert.AreEqual(0x04, pkt[34]);
            Assert.AreEqual(0xD2, pkt[35]);

            // Check UDP Dest Port (offset 36) - 5678 = 0x162E
            Assert.AreEqual(0x16, pkt[36]);
            Assert.AreEqual(0x2E, pkt[37]);
            
            // Check Payload (offset 42)
            Assert.AreEqual(0x11, pkt[42]);
            Assert.AreEqual(0x44, pkt[45]);
        }
