        [TestMethod]
        public void Convert_ValidRxLines_ParsesCorrectly()
        {
            string trcPath = GetTempFile(".trc");
            string outPath = GetTempFile(".pcap");
            try
            {
                string trcData = BuildMinimalTrc(new[]
                {
                    "       23.6  Rx     18F140CA  8  01 1E 00 28 00 00 00 00",
                    "       24.7  Rx     18F141CA  8  01 14 00 00 00 FF FF FF"
                });
                File.WriteAllText(trcPath, trcData);

                var opts = new TrcConversionOptions
                {
                    TrcFile = trcPath,
                    OutputPcap = outPath,
                    MetadataHeader = "TEST|1",
                    FramesPerPacket = 10
                };

                var res = TrcConverter.Convert(opts);

                Assert.AreEqual(2, res.FramesParsed);
                Assert.AreEqual(1, res.PacketsWritten);
                Assert.AreEqual(0, res.Warnings.Count);

                var payloads = ParsePcapUdpPayloads(outPath);
                Assert.AreEqual(1, payloads.Count);
                
                // Payload length = "TEST|1".Length (6) + (2 * 13) = 32
                Assert.AreEqual(32, payloads[0].Length);
            }
            finally
            {
                File.Delete(trcPath);
                if (File.Exists(outPath)) File.Delete(outPath);
            }
        }

        [TestMethod]
        public void Convert_InvalidLines_AddsWarningsAndContinues()
        {
            string trcPath = GetTempFile(".trc");
            string outPath = GetTempFile(".pcap");
            try
            {
                string trcData = BuildMinimalTrc(new[]
                {
                    "       23.6  Rx     18F140CA  8  01 1E 00 28 00 00 00 00", // valid
                    "       XX.X  Rx     18F141CA  8  00",                      // invalid time
                    "       25.0  Rx     NOTHEX    8  00",                      // invalid ID
                    "       26.0  Rx     100       9  00",                      // DLC > 8
                    "       27.0  Rx     18F141CA  8  01 14 00 00 00 FF FF FF"  // valid
                });
                File.WriteAllText(trcPath, trcData);

                var opts = new TrcConversionOptions { TrcFile = trcPath, OutputPcap = outPath };
                var res = TrcConverter.Convert(opts);

                Assert.AreEqual(2, res.FramesParsed);
                Assert.AreEqual(1, res.Warnings.Count); // Only the DLC > 8 line hits the warning. The rest fail the regex!
            }
            finally
            {
                File.Delete(trcPath);
                if (File.Exists(outPath)) File.Delete(outPath);
            }
        }

